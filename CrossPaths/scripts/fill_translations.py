def apply_map(locale: str, translations: dict[str, str]) -> int:
    po_path = BASE / locale / "LC_MESSAGES" / "messages.po"
    with po_path.open("rb") as f:
        catalog = pofile.read_po(f)

    updated = 0
    for message in catalog:
        if not isinstance(message.id, str) or not message.id:
            continue
        replacement = translations.get(message.id)
        if replacement is None:
            continue
        if message.string != replacement or "fuzzy" in message.flags:
            message.string = replacement
            if "fuzzy" in message.flags:
                message.flags.remove("fuzzy")
            updated += 1

    with po_path.open("wb") as f:
        pofile.write_po(f, catalog, width=80)

    return updated


